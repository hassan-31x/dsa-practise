
#! tree, node, leaf. ancestors, descendents, height, depth, balanced

#! tree: a collection of nodes

#! node: a single element of a tree that has a value and a reference to its parent and children

#! leaf: a node that has no children

#! ancestors: all the nodes on the path from the root to the node

#! descendents: all the nodes on the path from the node to the leaves

#! height: the number of edges on the longest path from the node to a leaf

#! depth: the number of edges on the path from the node to the root

#! balanced: a tree is balanced if the height of its left and right subtrees differ by at most 1

#! binary search tree: a tree which is sorted

#? for balanced binary search tree complexity: O(log(n)) for search, insert, delete